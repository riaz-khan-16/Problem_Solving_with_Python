# A hashmap is a data structure used in computer programming to efficiently store 
# and retrieve key-value pairs. It is also known as a hash table or dictionary.


# In Python, a hashmap can be implemented using a built-in data type called a dictionary. 
# A dictionary is a collection of key-value pairs, where each key is unique.


# create an empty dictionary
my_dict = {}

# add key-value pairs to the dictionary
my_dict["apple"] = 1
my_dict["banana"] = 2
my_dict["cherry"] = 3

# print the dictionary
print(my_dict)